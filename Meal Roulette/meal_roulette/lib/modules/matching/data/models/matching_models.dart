

class MatchingModel {
  final String name;
  final String date;
  final String location;
  final List<String> interests;
  final String email;
  final String phone;
  final String lunchDate;
  final String avatar;

  MatchingModel({
    required this.name,
    required this.date,
    required this.location,
    required this.interests,
    required this.email,
    required this.phone,
    required this.lunchDate,
    required this.avatar,
  });
}